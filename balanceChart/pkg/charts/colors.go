func darkenColor(c color.Color, factor float64) color.Color {
	r, g, b, a := c.RGBA()
	darken := func(value uint32) uint8 {
		return uint8(float64(value>>8) * factor)
	}

	return color.RGBA{
		R: darken(r),
		G: darken(g),
		B: darken(b),
		A: uint8(a >> 8),
	}
}
